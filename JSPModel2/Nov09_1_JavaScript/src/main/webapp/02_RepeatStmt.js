//반복문(Repeat Statement)
//		동일한 작업을 여러번 반복할때 사용

//for while do-while

//1~10까지 출력
for(let i=1; i<11 ;i++ ){
	console.log(i);
}

console.log('-----------------------')

let i1= 1;
while(i1 < 11){
	console.log(i1);
	i1++;
}

console.log('-----------------------')

let i2=1;
do{
	console.log(i2)
	i2++;
}while(i2<11)

